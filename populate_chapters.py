def _pick(scenes_key, idx, themes):
    pool = []
    for theme in themes:
        if theme in scenes_key:
            pool.extend(scenes_key[theme])
    if not pool:
        pool = list(scenes_key.values())[0] if scenes_key else [""]
    return pool[idx % len(pool)]


