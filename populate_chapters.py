def main():
    books = Book.objects.filter(is_deleted=False)
    total_created = 0
    total_skipped = 0
    
    for book in books:
        if not book.toc:
            print(f"  ⏭ {book.title} — 无目录数据，跳过")
            continue
        
        try:
            toc_list = json.loads(book.toc)
        except (json.JSONDecodeError, TypeError):
            print(f"  ⏭ {book.title} — 目录JSON解析失败")
            continue
        
        mode, themes = pick_mode(book.title)
        
        chapter_counter = 0
        for item in toc_list:
            chapter_title = item[0]
            page = item[1]
            chapter_idx = chapter_counter
            has_page = bool(page)

            if ChapterContent.objects.filter(book=book, chapter_index=chapter_idx).exists():
                total_skipped += 1
                chapter_counter += 1
                continue

            content = generate_chapter_content(
                book.title, chapter_title, chapter_idx,
                len(toc_list), mode, themes, has_page
            )
            
            ChapterContent.objects.create(
                book=book,
                chapter_index=chapter_idx,
                chapter_title=chapter_title,
                content=content,
            )
            chapter_counter += 1
            total_created += 1
        
        print(f"  ✓ {book.title} [{mode}] → {chapter_counter} 章")
    
    print(f"\n共创建 {total_created} 个章节, 跳过 {total_skipped} 个已存在章节")
    print(f"数据库中有 {ChapterContent.objects.count()} 条章节记录")


if __name__ == "__main__":
    main()